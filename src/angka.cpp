void tampilkanAngka(LedControl lc, int angka) {
  int puluhan = angka / 10;  // Mendapatkan digit puluhan
  int satuan = angka % 10;   // Mendapatkan digit satuan

  // Tampilkan digit pertama (puluhan)
  if (puluhan == 0) {
    tampilkanAngka0(lc); // Tampilkan angka 0 di digit pertama
  } else if (puluhan == 1) {
    tampilkanAngka1(lc); // Tampilkan angka 1 di digit pertama
  } else if (puluhan == 2) {
    tampilkanAngka2(lc); // Tampilkan angka 2 di digit pertama
  }

  // Tampilkan digit kedua (satuan)
  if (satuan == 0) {
    tampilkanAngka0DigitKedua(lc); // Tampilkan angka 0 di digit kedua
  } else if (satuan == 1) {
    tampilkanAngka1DigitKedua(lc); // Tampilkan angka 1 di digit kedua
  } else if (satuan == 2) {
    tampilkanAngka2DigitKedua(lc); // Tampilkan angka 2 di digit kedua
  } else if (satuan == 3) {
    tampilkanAngka3DigitKedua(lc); // Tampilkan angka 3 di digit kedua
  } else if (satuan == 4) {
    tampilkanAngka4DigitKedua(lc); // Tampilkan angka 4 di digit kedua
  } else if (satuan == 5) {
    tampilkanAngka5DigitKedua(lc); // Tampilkan angka 5 di digit kedua
  } else if (satuan == 6) {
    tampilkanAngka6DigitKedua(lc); // Tampilkan angka 6 di digit kedua
  } else if (satuan == 7) {
    tampilkanAngka7DigitKedua(lc); // Tampilkan angka 7 di digit kedua
  } else if (satuan == 8) {
    tampilkanAngka8DigitKedua(lc); // Tampilkan angka 8 di digit kedua
  } else if (satuan == 9) {
    tampilkanAngka9DigitKedua(lc); // Tampilkan angka 9 di digit kedua
  }
}

