void AngkaMirror(LedControl lc, int angka) {
  int puluhan = angka / 10;  // Mendapatkan digit puluhan
  int satuan = angka % 10;   // Mendapatkan digit satuan

  // Tampilkan digit pertama (puluhan)
  if (puluhan == 0) {
    AngkaMirror0(lc); // Tampilkan angka 0 di digit pertama
  } else if (puluhan == 1) {
    AngkaMirror1(lc); // Tampilkan angka 1 di digit pertama
  } else if (puluhan == 2) {
    AngkaMirror2(lc); // Tampilkan angka 2 di digit pertama
  }

  // Tampilkan digit kedua (satuan)
  if (satuan == 0) {
    AngkaMirror0DigitKedua(lc); // Tampilkan angka 0 di digit kedua
  } else if (satuan == 1) {
    AngkaMirror1DigitKedua(lc); // Tampilkan angka 1 di digit kedua
  } else if (satuan == 2) {
    AngkaMirror2DigitKedua(lc); // Tampilkan angka 2 di digit kedua
  } else if (satuan == 3) {
    AngkaMirror3DigitKedua(lc); // Tampilkan angka 3 di digit kedua
  } else if (satuan == 4) {
    AngkaMirror4DigitKedua(lc); // Tampilkan angka 4 di digit kedua
  } else if (satuan == 5) {
    AngkaMirror5DigitKedua(lc); // Tampilkan angka 5 di digit kedua
  } else if (satuan == 6) {
    AngkaMirror6DigitKedua(lc); // Tampilkan angka 6 di digit kedua
  } else if (satuan == 7) {
    AngkaMirror7DigitKedua(lc); // Tampilkan angka 7 di digit kedua
  } else if (satuan == 8) {
    AngkaMirror8DigitKedua(lc); // Tampilkan angka 8 di digit kedua
  } else if (satuan == 9) {
    AngkaMirror9DigitKedua(lc); // Tampilkan angka 9 di digit kedua
  }
}

